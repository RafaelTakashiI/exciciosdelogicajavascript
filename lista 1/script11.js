//Escreva um algoritmo para ler as dimensões de um trapézio (base maior, base menor e altura), calcular e escrever a área do trapézio. a=(B+b).h/2

let basemaior = parseInt(prompt("Digite o valor da base maior: "));
let basemenor = parseInt(prompt("Digite o valor da base menor: "));
let altura = parseInt(prompt("Digite o valor da altura: "));
area = (basemaior+basemenor)*altura/2;
alert ("O valor da área do trapézio é: "+area)

